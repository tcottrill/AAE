const struct RomModule
{
	const char* filename;
	int loadAddr;
	int romSize;
	int loadtype;
	//Hashing Checksums
	unsigned int crc;
	const char* sha;
};

#define COMMA ,
#define CRC(n)            (0x ## n) 
#define SHA1(x)           COMMA#x
#define ROM_LOAD_NORMAL 0
#define ROM_LOAD_16B    1
#define ROM_REGION_START 999

#define ROM_START(name) static struct RomModule rom_##name[] = {
#define ROM_REGION( romSize, loadtype) { NULL, 0x999, romSize, loadtype },
#define ROM_LOAD(filename,loadAddr,romSize) { filename,loadAddr,romSize, ROM_LOAD_NORMAL },

#define ROM_START(name) static struct RomModule rom_##name[] = {
#define ROM_REGION( romSize, loadtype) { NULL, ROM_REGION_START, romSize, loadtype }, 
#define ROM_LOAD(filename,loadAddr,romSize, crc, sha) { filename,loadAddr,romSize, ROM_LOAD_NORMAL, crc , sha },
