// ============================================================================
// Color PROM conversion
//
// Xevious has three 256x4 palette PROMs (one per gun) and four 512x4 lookup
// table PROMs (two for sprites, two for background tiles; foreground
// characters map directly to a palette color).
//
// bit 3 -- 220 ohm resistor  -- RED/GREEN/BLUE
//       -- 470 ohm resistor  -- RED/GREEN/BLUE
//       -- 1  kohm resistor  -- RED/GREEN/BLUE
// bit 0 -- 2.2kohm resistor  -- RED/GREEN/BLUE
//
// NOTE: This function uses the embedded color_prom[] array above instead of
// the passed-in color_prom parameter.  The AAE raster system calls this with
// memory_region(REGION_PROMS), but since we have the data embedded we ignore
// that pointer and walk our own static array -- exactly as MAME 0.33 did
// before color PROM loading was standardized.
// ============================================================================

void xevious_vh_convert_color_prom(unsigned char* palette, unsigned char* colortable,
                                   const unsigned char* /*color_prom_unused*/)
{
    int i;
    const unsigned char* cp = color_prom;   // use the embedded static array

#define TOTAL_COLORS(gfxn) (Machine->gfx[gfxn]->total_colors * Machine->gfx[gfxn]->color_granularity)
#define COLOR(gfxn, offs) (colortable[Machine->drv->gfxdecodeinfo[gfxn].color_codes_start + offs])

    for (i = 0; i < 128; i++)
    {
        int bit0, bit1, bit2, bit3;

        // red
        bit0 = (cp[0] >> 0) & 0x01;
        bit1 = (cp[0] >> 1) & 0x01;
        bit2 = (cp[0] >> 2) & 0x01;
        bit3 = (cp[0] >> 3) & 0x01;
        palette[3 * i + 0] = (unsigned char)(0x0e * bit0 + 0x1f * bit1 + 0x43 * bit2 + 0x8f * bit3);

        // green
        bit0 = (cp[256] >> 0) & 0x01;
        bit1 = (cp[256] >> 1) & 0x01;
        bit2 = (cp[256] >> 2) & 0x01;
        bit3 = (cp[256] >> 3) & 0x01;
        palette[3 * i + 1] = (unsigned char)(0x0e * bit0 + 0x1f * bit1 + 0x43 * bit2 + 0x8f * bit3);

        // blue
        bit0 = (cp[2 * 256] >> 0) & 0x01;
        bit1 = (cp[2 * 256] >> 1) & 0x01;
        bit2 = (cp[2 * 256] >> 2) & 0x01;
        bit3 = (cp[2 * 256] >> 3) & 0x01;
        palette[3 * i + 2] = (unsigned char)(0x0e * bit0 + 0x1f * bit1 + 0x43 * bit2 + 0x8f * bit3);

        cp++;
    }

    // Color 0x80 is used by sprites to mark transparency
    palette[3 * 128 + 0] = 1;
    palette[3 * 128 + 1] = 1;
    palette[3 * 128 + 2] = 1;

    cp += 128;          // skip unused bottom half of palette PROM
    cp += 2 * 256;      // now points to lookup tables

    // Background tiles (GFX index 1)
    for (i = 0; i < TOTAL_COLORS(1); i++)
        COLOR(1, i) = *(cp++);

    // Sprites (GFX indices 2, 3, 4 share the same color table)
    for (i = 0; i < TOTAL_COLORS(2); i++)
    {
        if (i % 8 == 0)
            COLOR(2, i) = 0x80;    // transparent
        else
            COLOR(2, i) = *cp;

        cp++;
    }

    // Copy same sprite color table for sets 3 and 4
    for (i = 0; i < TOTAL_COLORS(3); i++)
        COLOR(3, i) = COLOR(2, i % TOTAL_COLORS(2));
    for (i = 0; i < TOTAL_COLORS(4); i++)
        COLOR(4, i) = COLOR(2, i % TOTAL_COLORS(2));

    // Foreground characters (GFX index 0)
    for (i = 0; i < TOTAL_COLORS(0); i++)
    {
        if (i % 2 == 0)
            COLOR(0, i) = 0x80;    // transparent
        else
            COLOR(0, i) = (unsigned char)(i / 2);
    }

#undef TOTAL_COLORS
#undef COLOR
}


// ============================================================================
// Video latch write (scroll registers, flip)
// ============================================================================

void xevious_vh_latch_w_impl(int offset, int data)
{
    int val;
    int reg;

    val = data + ((offset & 0x01) << 8);   // A0 -> D8
    reg = (offset & 0xf0) >> 4;

    switch (reg)
    {
    case 0: bg_y_pos = val; break;
    case 2: bg_x_pos = val; break;
    case 1: fo_y_pos = val; break;
    case 3: fo_x_pos = val; break;
    case 7: flip = val & 1; break;
    default: break;
    }
}


// ============================================================================
// Background VRAM write-through handlers
// ============================================================================

void xevious_videoram2_w_impl(int offset, int data)
{
    if (xevious_videoram2[offset] != (unsigned char)data)
    {
        dirtybuffer2[offset] = 1;
        xevious_videoram2[offset] = (unsigned char)data;
    }
}

void xevious_colorram2_w_impl(int offset, int data)
{
    if (xevious_colorram2[offset] != (unsigned char)data)
    {
        dirtybuffer2[offset] = 1;
        xevious_colorram2[offset] = (unsigned char)data;
    }
}


// ============================================================================
// vh_start / vh_stop
// ============================================================================

int xevious_vh_start(void)
{
    videoram_size = 0x800;

    if ((dirtybuffer = (unsigned char*)malloc(videoram_size)) == nullptr)
        return 1;
    memset(dirtybuffer, 1, videoram_size);

    if ((dirtybuffer2 = (unsigned char*)malloc(videoram_size)) == nullptr)
    {
        free(dirtybuffer);
        dirtybuffer = nullptr;
        return 1;
    }
    memset(dirtybuffer2, 1, videoram_size);

    // Foreground tilemap cache (32 tiles wide x 64 tiles tall)
    if ((tmpbitmap1 = osd_create_bitmap(32 * 8, 64 * 8)) == nullptr)
    {
        free(dirtybuffer2);
        free(dirtybuffer);
        dirtybuffer2 = nullptr;
        dirtybuffer = nullptr;
        return 1;
    }

    // Background tilemap cache
    if ((tmpbitmap2 = osd_create_bitmap(32 * 8, 64 * 8)) == nullptr)
    {
        osd_free_bitmap(tmpbitmap1);
        tmpbitmap1 = nullptr;
        free(dirtybuffer2);
        free(dirtybuffer);
        dirtybuffer2 = nullptr;
        dirtybuffer = nullptr;
        return 1;
    }

    bg_y_pos = 0;
    bg_x_pos = 0;
    fo_y_pos = 0;
    fo_x_pos = 0;
    flip = 0;

    return 0;
}

void xevious_vh_stop(void)
{
    if (tmpbitmap2)
    {
        osd_free_bitmap(tmpbitmap2);
        tmpbitmap2 = nullptr;
    }
    if (tmpbitmap1)
    {
        osd_free_bitmap(tmpbitmap1);
        tmpbitmap1 = nullptr;
    }
    if (dirtybuffer2)
    {
        free(dirtybuffer2);
        dirtybuffer2 = nullptr;
    }
    if (dirtybuffer)
    {
        free(dirtybuffer);
        dirtybuffer = nullptr;
    }
}


// ============================================================================
// Screen refresh
// ============================================================================

void xevious_vh_screenrefresh(void)
{
    int offs, sx, sy;

    // --- Update tile caches ---
    for (offs = videoram_size - 1; offs >= 0; offs--)
    {
        // Foreground
        if (dirtybuffer[offs])
        {
            dirtybuffer[offs] = 0;

            sx = 31 - (offs / 64);
            sy = offs % 64;

            drawgfx(tmpbitmap1, Machine->gfx[0],
                    videoram[offs],
                    ((colorram[offs] & 0x03) << 4) + ((colorram[offs] >> 2) & 0x0f),
                    colorram[offs] & 0x80, colorram[offs] & 0x40,
                    8 * sx, 8 * sy,
                    0, TRANSPARENCY_NONE, 0);
        }

        // Background
        if (dirtybuffer2[offs])
        {
            dirtybuffer2[offs] = 0;

            sx = 31 - (offs / 64);
            sy = offs % 64;

            drawgfx(tmpbitmap2, Machine->gfx[1],
                    xevious_videoram2[offs] + 256 * (xevious_colorram2[offs] & 1),
                    ((xevious_colorram2[offs] >> 2) & 0xf) +
                        ((xevious_colorram2[offs] & 0x3) << 5) +
                        ((xevious_videoram2[offs] & 0x80) >> 3),
                    xevious_colorram2[offs] & 0x80, xevious_colorram2[offs] & 0x40,
                    8 * sx, 8 * sy,
                    0, TRANSPARENCY_NONE, 0);
        }
    }

    // --- Copy background with scroll ---
    {
        int scrollx = bg_x_pos - 16;
        int scrolly = -bg_y_pos - 20;

        copyscrollbitmap(main_bitmap, tmpbitmap2,
                         1, &scrollx, 1, &scrolly,
                         &Machine->drv->visible_area,
                         TRANSPARENCY_NONE, 0);
    }

    // --- Draw sprites ---
    for (offs = 0; offs < spriteram_size; offs += 2)
    {
        if ((spriteram[offs + 1] & 0x40) == 0)
        {
            int bank, code, color, flipx, flipy;

            bank  = 2 + ((spriteram[offs] & 0x80) >> 7)
                      + ((spriteram_3[offs] & 0x80) >> 6);
            code  = spriteram[offs] & 0x7f;
            color = spriteram[offs + 1] & 0x7f;
            flipx = spriteram_3[offs] & 4;
            flipy = spriteram_3[offs + 1] & 4;
            sx    = spriteram_2[offs] - 15;
            sy    = spriteram_2[offs + 1] - 40
                    + 0x100 * (spriteram_3[offs + 1] & 1);

            if (spriteram_3[offs] & 2)  // double width
            {
                if (spriteram_3[offs] & 1)  // double width + double height
                {
                    code &= 0x7c;
                    drawgfx(main_bitmap, Machine->gfx[bank],
                            code + 3, color, flipx, flipy,
                            flipx ? sx + 16 : sx, flipy ? sy : sy + 16,
                            &Machine->drv->visible_area, TRANSPARENCY_COLOR, 0x80);
                    drawgfx(main_bitmap, Machine->gfx[bank],
                            code + 1, color, flipx, flipy,
                            flipx ? sx : sx + 16, flipy ? sy : sy + 16,
                            &Machine->drv->visible_area, TRANSPARENCY_COLOR, 0x80);
                }
                code &= 0x7d;
                drawgfx(main_bitmap, Machine->gfx[bank],
                        code + 2, color, flipx, flipy,
                        flipx ? sx + 16 : sx, flipy ? sy + 16 : sy,
                        &Machine->drv->visible_area, TRANSPARENCY_COLOR, 0x80);
                drawgfx(main_bitmap, Machine->gfx[bank],
                        code, color, flipx, flipy,
                        flipx ? sx : sx + 16, flipy ? sy + 16 : sy,
                        &Machine->drv->visible_area, TRANSPARENCY_COLOR, 0x80);
            }
            else if (spriteram_3[offs] & 1)  // double height
            {
                code &= 0x7e;
                drawgfx(main_bitmap, Machine->gfx[bank],
                        code, color, flipx, flipy,
                        flipx ? sx + 16 : sx, flipy ? sy + 16 : sy,
                        &Machine->drv->visible_area, TRANSPARENCY_COLOR, 0x80);
                drawgfx(main_bitmap, Machine->gfx[bank],
                        code + 1, color, flipx, flipy,
                        flipx ? sx + 16 : sx, flipy ? sy : sy + 16,
                        &Machine->drv->visible_area, TRANSPARENCY_COLOR, 0x80);
            }
            else  // normal 16x16
            {
                drawgfx(main_bitmap, Machine->gfx[bank],
                        code, color, flipx, flipy,
                        sx, sy,
                        &Machine->drv->visible_area, TRANSPARENCY_COLOR, 0x80);
            }
        }
    }

    // --- Copy foreground with scroll ---
    {
        int scrollx = fo_x_pos - 14;
        int scrolly = -fo_y_pos - 32;

        copyscrollbitmap(main_bitmap, tmpbitmap1,
                         1, &scrollx, 1, &scrolly,
                         &Machine->drv->visible_area,
                         TRANSPARENCY_COLOR, 0x80);
    }
}
