// Intel 8085A Emulator for AAE (Another Arcade Emulator)
//
// Based on the AAE cpu_i8080 emulator written by Mike Chambers, extended to full 8085A.,
// extended to full 8085A with:
//   - RIM / SIM instructions
//   - Vectored interrupt system (TRAP, RST 5.5, RST 6.5, RST 7.5, INTR)
//   - SID / SOD serial I/O bits
//   - 8085-correct cycle timings
//   - HALT state with interrupt wake-up
//   - V (overflow) flag support on flag bit 1 (undocumented but real)
//
// Reference material:
//   - MAME I8085 emulator by Juergen Buchmueller
//   - Intel 8085A datasheet
//   - AAE cpu_z80 for structural patterns
//
// The interface (constructor, exec, interrupt, reset, get_ticks, In, Out,
// memory handler pointers) mirrors cpu_i8080 for drop-in compatibility
// with AAE's cpu_control framework.
// -----------------------------------------------------------------------------

