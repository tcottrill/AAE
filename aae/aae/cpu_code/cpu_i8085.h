class cpu_i8085
{

public:

	// ----- Flag bit definitions -----
	static constexpr uint8_t SF = 0x80;  // Sign
	static constexpr uint8_t ZF = 0x40;  // Zero
	static constexpr uint8_t X5F = 0x20; // Undocumented flag bit 5
	static constexpr uint8_t HF = 0x10;  // Half-carry (Auxiliary carry)
	static constexpr uint8_t X3F = 0x08; // Undocumented flag bit 3
	static constexpr uint8_t VF = 0x04;  // Overflow / Parity (bit 2)
	static constexpr uint8_t NF = 0x02;  // Undocumented N flag (subtract)
	static constexpr uint8_t CF = 0x01;  // Carry

	// ----- Interrupt mask register bit definitions -----
	static constexpr uint8_t IM_SID    = 0x80;  // Serial Input Data
	static constexpr uint8_t IM_SOD    = 0x40;  // Serial Output Data
	static constexpr uint8_t IM_IEN    = 0x20;  // Interrupt Enable
	static constexpr uint8_t IM_TRAP   = 0x10;  // TRAP pending
	static constexpr uint8_t IM_INTR   = 0x08;  // INTR mask
	static constexpr uint8_t IM_RST75  = 0x04;  // RST 7.5 mask
	static constexpr uint8_t IM_RST65  = 0x02;  // RST 6.5 mask
	static constexpr uint8_t IM_RST55  = 0x01;  // RST 5.5 mask

	// ----- Interrupt vector addresses -----
	static constexpr uint16_t ADDR_TRAP   = 0x0024;
	static constexpr uint16_t ADDR_RST55  = 0x002C;
	static constexpr uint16_t ADDR_RST65  = 0x0034;
	static constexpr uint16_t ADDR_RST75  = 0x003C;
	static constexpr uint16_t ADDR_INTR   = 0x0038;

	// ----- Interrupt type constants for cause_interrupt() -----
	static constexpr int I8085_INTR  = 0xFF;
	static constexpr int I8085_SID   = 0x10;
	static constexpr int I8085_RST75 = 0x08;
	static constexpr int I8085_RST65 = 0x04;
	static constexpr int I8085_RST55 = 0x02;
	static constexpr int I8085_TRAP  = 0x01;
	static constexpr int I8085_NONE  = 0x00;

	// ----- Registers -----
	uint8_t reg8[9] = { 0,0,0,0,0,0,0,0,0 };
	uint16_t reg_SP = 0;
	uint16_t reg_PC = 0;

	#define i85_reg16_PSW (((uint16_t)reg8[A] << 8) | (uint16_t)reg8[FLAGS])
	#define i85_reg16_BC  (((uint16_t)reg8[B] << 8) | (uint16_t)reg8[C])
	#define i85_reg16_DE  (((uint16_t)reg8[D] << 8) | (uint16_t)reg8[E])
	#define i85_reg16_HL  (((uint16_t)reg8[H] << 8) | (uint16_t)reg8[L])

	enum {
		B, C, D, E, H, L, M, A, FLAGS
	};

	// ----- 8085 interrupt/serial state -----
	uint8_t IM = 0;      // Interrupt mask register
	uint8_t IREQ = 0;    // Pending interrupt request flags
	uint8_t ISRV = 0;    // Currently serviced interrupt
	uint16_t IRQ2 = 0;   // Scheduled interrupt address
	uint16_t IRQ1 = 0;   // Executing interrupt address
	int HALT = 0;         // HALT state

	// Pointer to the cpu memory map
	uint8_t *MEM;
	// Pointer to the handler structures
	MemoryReadByte *memory_read = nullptr;
	MemoryWriteByte *memory_write = nullptr;
	z80PortRead *z80IoRead = nullptr;
	z80PortWrite *z80IoWrite = nullptr;

	// Constructor - same signature as cpu_i8080 for cpu_control compatibility
	cpu_i8085(uint8_t* mem, MemoryReadByte* read_mem, MemoryWriteByte* write_mem,
	          z80PortRead* port_read, z80PortWrite* port_write, uint16_t addr);

	// Destructor
	~cpu_i8085() {};

	// ----- Public interface (matches cpu_i8080) -----
	uint8_t In(uint8_t bPort);
	void Out(uint8_t bPort, uint8_t bVal);
	int  exec(int cycles);
	int  get_ticks(int reset);
	void reset();

	// 8080-compatible interrupt (for cpu_control cpu_do_int_imm)
	void interrupt(uint8_t n);

	// 8085 extended interrupt interface
	void cause_interrupt(int type);
	void clear_pending_interrupts();
	void set_SID(int state);

	// Optional SOD callback
	void (*SOD_callback)(int state) = nullptr;
	void set_SOD_callback(void (*callback)(int state)) { SOD_callback = callback; }

	// MAME-style memory handling options
	void mame_memory_handling(bool s) { mmem = s; }
	void log_unhandled_rw(bool s) { log_debug_rw = s; }

private:

	// Internal Memory handlers
	uint8_t i8085_read(uint16_t addr);
	void    i8085_write(uint16_t addr, uint8_t byte);

	// Register pair helpers
	uint16_t read_RP(uint8_t rp);
	uint16_t read_RP_PUSHPOP(uint8_t rp);
	void write_RP(uint8_t rp, uint8_t lb, uint8_t hb);
	void write16_RP(uint8_t rp, uint16_t value);
	void write16_RP_PUSHPOP(uint8_t rp, uint16_t value);

	// Register access (M goes through memory)
	void write_reg8(uint8_t reg, uint8_t value);
	uint8_t read_reg8(uint8_t reg);

	// Condition testing
	uint8_t test_cond(uint8_t code);

	// Stack operations
	void i8085_push(uint16_t value);
	uint16_t i8085_pop();

	int SID_state = 0; // Current state of the SID pin
	int SOD_state = 0; // Current state of the SOD pin
	// Note: Use IREQ bit 6 for RST 7.5, bit 5 for 6.5, bit 4 for 5.5 
	// to make RIM reporting easier.

	// 8085 interrupt handling internals
	void set_TRAP(int state);
	void set_RST75(int state);
	void set_RST65(int state);
	void set_RST55(int state);
	void set_INTR(int type);
	void do_interrupt();

	bool debug = 0;
	bool mmem = 0;
	int log_debug_rw = 0;
	int clocktickstotal = 0;
};

