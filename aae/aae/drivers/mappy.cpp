// ---------------------------------------------------------------------------
// Interrupt enable / generate for both CPUs
// The hardware uses address lines (not data) to set enable state.
// Writing to offset 2 enables; writing to offset 3 disables (or vice versa,
// the exact polarity is captured in the offset value the MAME source uses).
// ---------------------------------------------------------------------------

WRITE_HANDLER(mappy_interrupt_enable_1_w)
{
    interrupt_enable_1 = address & 1;
}

WRITE_HANDLER(mappy_interrupt_enable_2_w)
{
    interrupt_enable_2 = address & 1;
}

/* CPU1 VBLANK interrupt callback */
void mappy_interrupt_1(void)
{
    if (interrupt_enable_1)
        cpu_do_int_imm(CPU0, INT_TYPE_INT);
}

/* CPU2 VBLANK interrupt callback */
void mappy_interrupt_2(void)
{
    if (interrupt_enable_2)
        cpu_do_int_imm(CPU1, INT_TYPE_INT);
}

/* CPU2 (sound CPU) halt control.  offset 0 = halt, offset 1 = run */
WRITE_HANDLER(mappy_cpu_enable_w)
{
    /* address 0x500a = halt sound CPU, 0x500b = enable sound CPU */
    cpu_enable(CPU1, address & 1);
}


// ---------------------------------------------------------------------------
// Sound register write (CPU2 maps Namco sound at 0x0000-0x003f)
// ---------------------------------------------------------------------------

WRITE_HANDLER(mappy_sound_w)
{
    mappy_sound_w(address, data);
 }

/* sound enable register -- just pass through to Namco core */
WRITE_HANDLER(mappy_sound_enable_w)
{
    /* address offset 0 = disable, 1 = enable; data is unused by hardware */
    mappy_sound_enable_w(address, data);
}


// ---------------------------------------------------------------------------
// Video RAM / color RAM write-through handlers (mark dirty buffer entries)
// ---------------------------------------------------------------------------

WRITE_HANDLER(mappy_videoram_w)
{
    if (videoram[address] != (unsigned char)data)
    {
        dirtybuffer[address] = 1;
        videoram[address] = (unsigned char)data;
    }
}

WRITE_HANDLER(mappy_colorram_w)
{
    if (colorram[address] != (unsigned char)data)
    {
        dirtybuffer[address] = 1;
        colorram[address] = (unsigned char)data;
    }
}

/* scroll register: the hardware maps a 0x800-byte window, with the actual
   scroll value encoded in the upper address bits (>>3) */
WRITE_HANDLER(mappy_scroll_w)
{
    mappy_scroll = (unsigned char)(address >> 3);
}


