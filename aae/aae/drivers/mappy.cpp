/***************************************************************************

  mappy.cpp  --  AAE driver for Mappy and related Namco games

  Hardware family: Namco Super Pac-Man board (dual 6809, custom I/O, Namco sound)
  Games covered:
    mappy     - Mappy (US)           1983 Namco
    mappyjp   - Mappy (Japan)        1983 Namco
    digdug2   - Dig Dug 2            1985 Namco
    motos     - Motos                1985 Namco
    todruaga  - Tower of Druaga      1984 Namco

  Memory map (CPU 1):
    0000-07ff  video RAM
    0800-0fff  color RAM
    1000-177f  general RAM
    1780-17ff  sprite RAM area 1 (sprite number and color)
    1800-1f7f  general RAM
    1f80-1fff  sprite RAM area 2 (x, y position)
    2000-277f  general RAM
    2780-27ff  sprite RAM area 3 (high bit of y, flip flags, size flags)
    3800-3fff  scroll register map
    4040-43ff  RAM shared with CPU 2 (sound CPU)
    4800-480f  custom I/O chip 1
    4810-481f  custom I/O chip 2
    5002-5003  CPU 1 interrupt enable
    500a-500b  CPU 2 (sound) enable
    8000       watchdog timer
    a000-ffff  ROM (mappy), 8000-ffff (digdug2, motos, todruaga)

  Memory map (CPU 2 / sound):
    0000-003f  Namco sound registers
    0040-03ff  RAM shared with CPU 1
    2000-2001  CPU 2 interrupt enable
    2006-2007  sound enable
    e000-ffff  ROM

  Interrupts:
    CPU 1 IRQ: generated by VBLANK
    CPU 2 IRQ: generated by VBLANK

  Converted from MAME 0.33 mappy driver by Aaron Giles / Mirko Buffoni / JROK
 
***************************************************************************/

