// ---------------------------------------------------------------------------
// Mappy custom I/O read -- chip 1 (joystick, coins, start, credits)
// ---------------------------------------------------------------------------

READ_HANDLER(mappy_customio_r_1)
{
    /* credit/coin ratios for each DSW1 coin setting */
    static int crednum[] = { 1, 2, 3, 6, 1, 3, 1, 2 };
    static int credden[] = { 1, 1, 1, 1, 2, 2, 3, 3 };
    int val, temp;
    int mode = mappy_customio_1[8];

    /* mode 1 and 3 return live gameplay values */
    if (mode == 1 || mode == 3)
    {
        switch (address)
        {
        case 0:     /* coin slots, low nibble of port 4 */
        {
            static int lastval;
            val = readinputport(4) & 0x0f;
            if ((val & 1) && ((val ^ lastval) & 1)) ++credits;
            return lastval = val;
        }
        case 1:     /* start buttons, high nibble of port 4 */
        {
            static int lastval;
            temp = readinputport(1) & 7;
            val  = readinputport(4) >> 4;
            if ((val & 1) && ((val ^ lastval) & 1))
            {
                if (credits >= credden[temp]) credits -= credden[temp];
                else val &= ~1;
            }
            if ((val & 2) && ((val ^ lastval) & 2))
            {
                if (credits >= 2 * credden[temp]) credits -= 2 * credden[temp];
                else val &= ~2;
            }
            return lastval = val;
        }
        case 2:     /* high BCD digit of credit display */
            temp = readinputport(1) & 7;
            return (credits * crednum[temp] / credden[temp]) / 10;
        case 3:     /* low BCD digit of credit display */
            temp = readinputport(1) & 7;
            return (credits * crednum[temp] / credden[temp]) % 10;
        case 4:     /* player 1 joystick */
            return readinputport(3) & 0x0f;
        case 5:     /* player 1 buttons */
            return readinputport(3) >> 4;
        case 6:
        case 7:
            return 0;
        }
    }
    /* mode 5 returns self-test verification values used during power-on */
    else if (mode == 5)
    {
        static int testvals[] = { 8, 4, 6, 14, 13, 9, 13 };
        if (address >= 1 && address <= 7)
            return testvals[address - 1];
    }

    return mappy_customio_1[address];
}


// ---------------------------------------------------------------------------
// Mappy custom I/O read -- chip 2 (DIP switches, service)
// ---------------------------------------------------------------------------

READ_HANDLER(mappy_customio_r_2)
{
    int mode = mappy_customio_2[8];

    if (mode == 4)
    {
        switch (address)
        {
        case 0:     return readinputport(1) & 0x0f;     /* DSW1 low nibble  */
        case 1:     return readinputport(1) >> 4;       /* DSW1 high nibble */
        case 2:     return readinputport(0) & 0x0f;     /* DSW0 low nibble  */
        case 4:     return readinputport(0) >> 4;       /* DSW0 high nibble */
        case 6:     return readinputport(2) & 0x0f;     /* DSW2 / service   */
        case 3:
        case 5:
        case 7:     return 0;
        }
    }
    else if (mode == 5)
    {
        static int testvals[] = { 8, 4, 6, 14, 13, 9, 13 };
        if (address >= 1 && address <= 7)
            return testvals[address - 1];
    }

    return mappy_customio_2[address];
}


