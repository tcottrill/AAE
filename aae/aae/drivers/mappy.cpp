int init_mappy(void)
{
    /* video RAM layout: chars at 0x0000, sprites in three banks */
    videoram       = &Machine->memory_region[CPU0][0x0000];
    videoram_size  = 0x0800;
    colorram       = &Machine->memory_region[CPU0][0x0800];

    spriteram      = &Machine->memory_region[CPU0][0x1780];
    spriteram_size = 0x0080;
    spriteram_2    = &Machine->memory_region[CPU0][0x1f80];
    spriteram_3    = &Machine->memory_region[CPU0][0x2780];

    /* point shared RAM at CPU1 address space starting at 0x4040 */
    mappy_sharedram = &Machine->memory_region[CPU0][0x4040];
    mappy_customio_1 = &Machine->memory_region[CPU0][0x4800];
    mappy_customio_2 = &Machine->memory_region[CPU0][0x4810];

    /* start sound CPU halted; main CPU will enable it via mappy_cpu_enable_w */
    cpu_enable(CPU1, 0);

    mappy_init_machine();
    mappy_vh_start();
    namco_sh_start(&namco_interface);

    interrupt_enable_1 = 0;
    interrupt_enable_2 = 0;
    cpu_setOPbaseoverride(nullptr);

    return 1;
}

void end_mappy(void)
{
    mappy_vh_stop();
}

